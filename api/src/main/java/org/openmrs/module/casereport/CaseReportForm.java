package org.openmrs.module.casereport;

import java.util.List;

/**
 * An instance of this class encapsulates the serialized report form data
 */
public class CaseReportForm {
	
	private String givenName;
	
	private String middleName;
	
	private String familyName;
	
	private String gender;
	
	private String birthDate;
	
	private List<String> reportTriggers;
	
	public String getBirthDate() {
		return birthDate;
	}
	
	public void setBirthDate(String birthDate) {
		this.birthDate = birthDate;
	}
	
	public String getGender() {
		return gender;
	}
	
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	public String getGivenName() {
		return givenName;
	}
	
	public void setGivenName(String givenName) {
		this.givenName = givenName;
	}
	
	public String getMiddleName() {
		return middleName;
	}
	
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	
	public String getFamilyName() {
		return familyName;
	}
	
	public void setFamilyName(String familyName) {
		this.familyName = familyName;
	}
	
	public List<String> getReportTriggers() {
		return reportTriggers;
	}
	
	public void setReportTriggers(List<String> reportTriggers) {
		this.reportTriggers = reportTriggers;
	}
}
